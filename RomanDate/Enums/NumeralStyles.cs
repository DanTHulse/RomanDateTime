namespace RomanDate.Enums
{
    /// <summary>
    /// Enum used to represent the different styles of Roman numerals
    /// </summary>
    public enum NumeralStyles
    {
        /// <summary>
        /// The subtractive style (i.e. 4 = IV)
        /// </summary>
        Subtractive = 0,

        /// <summary>
        /// The additive style (i.e. 4 = IIII)
        /// </summary>
        Additive = 1
    }
}
