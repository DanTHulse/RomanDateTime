namespace RomanDate.Enums
{
    /// <summary>
    /// Enum used to represent the sacred days (set days) used in the Roman calendar
    /// </summary>
    public enum SetDays
    {
        /// <summary>
        /// First Day of the Month (falls on the 1st of the Month)
        /// </summary>
        Kalendae = 1,

        /// <summary>
        /// Ninth Day before the Idus of the Month (falls on the 5th or 7th of the Month)
        /// </summary>
        Nonae = 2,

        /// <summary>
        /// Middle Day of the Month (falls on the 13th or 15th of the Month)
        /// </summary>
        Idus = 3
    }
}
